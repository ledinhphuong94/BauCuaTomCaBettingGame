export default function renderImage(id) {
    switch (id) {
        case 1:
            return './img/cua.png'

        case 2:
            return './img/nai.png'

        case 3:
            return './img/ga.png'

        case 4:
            return './img/tom.png'

        case 5:
            return './img/ca.png'
        
        case 6:
            return './img/bau.png'

        default:
            break;
    }
}
